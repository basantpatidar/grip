package gripOne;

public class Car {
	private int wheels;
	private int doors;
	private int mgp;
	public int getWheels() {
		return wheels;
	}
	public void setWheels(int wheels) {
		this.wheels = wheels;
	}
	public int getDoors() {
		return doors;
	}
	public void setDoors(int doors) {
		this.doors = doors;
	}
	public int getMgp() {
		return mgp;
	}
	public void setMgp(int mgp) {
		this.mgp = mgp;
	}
}
